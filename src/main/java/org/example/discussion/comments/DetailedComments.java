package org.example.discussion.comments;

public class DetailedComments {

    /// <summary>
    /// This class describes a user.
    /// </summary>
    class User {

        /// <summary>
        /// Initialises a new User instance.
        /// </summary>
        public User() {
        }

        /// <summary>
        /// This is the user unique identifier.
        /// </summary>
        public int id;

        /// <summary>
        /// This is the user name.
        /// </summary>
        public String name;
    }

}
